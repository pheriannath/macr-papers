      <p>
        Child’s advice may have been echoing in Lewis’s mind as the very real
        problem of finding buyers for her prolific monumental marble sculptures
        began to appear. Though successful early on in finding placements for
        works appealing to abolitionist sentiments, Lewis developed a habit of
        sending sculptures across the Atlantic on speculation—seemingly not at
        all bothered whether the patrons she assumed would be purchasing them
        would indeed accept delivery and reimburse her even for the cost of the
        marble. In a paradigmatic letter to abolitionist and supporter Maria
        Weston Chapman in 1867, Lewis mused on how simple her practice could be
        if only “every black (sic) man in the United States would give a penny
        each” to help pay for the sculpture she wanted to send to abolitionist
        patriarch William Garrison.
        <sup>
          <a href="#annotation-15">15</a>
        </sup>{" "}
        This was an unusual business model, and certainly indicative of a
        natural salesperson committed to the hustle. Child, likely feeling that
        vindication was at hand for her ignored advice, refused to help find
        placements for some of these works. For the sculptor, however, it may
        not have been hubris driving this practice, but her experience with the
        tourist trade. An Ojibwe craftswoman would not wait for a tourist to
        come along and politely request the production of a pair of moccasins.
        They would be made in advance, ready and waiting with eye-catching
        embroidery to entice a buyer, perhaps even pushed plaintively from
        wagons laden with wares. In this context, the sending of a sculpture
        overseas in pursuit of a buyer seems not foolhardy but like a sound if
        perhaps uncouth business practice: If you build it, they will buy.{" "}
      </p>

